import java.io.IOException;

public class Main {

	public static void main(String[] args) throws IOException {

		for(int i=0;i<args.length;i++) {
			System.out.println(args[i]);
		}

		if (args[0].equalsIgnoreCase("TST"))
		{
			try
			{
				TST<Integer> tree = new TST<Integer>();
				String input = args[1];
				for (int i = 2; i < args.length; i++) {
					input = input + " " + args[i];
				}
				tree.activateSearchSystem(input.toUpperCase());
			}
			catch(ArrayIndexOutOfBoundsException e)
			{
				System.out.println("Enter TST followed by a bus stop name to be searched");
			}
		}

		else
		{
			System.out.println("Invalid Input. Enter SP or TST or SBAT");
		}
	}

}
